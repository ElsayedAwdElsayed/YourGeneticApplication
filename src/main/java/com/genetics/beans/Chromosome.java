public class Chromosome {
    private int numberOfCars;
    private int numberOfFridges;
    private int numberOfTanks;

    public int getNumberOfCars() {
        return numberOfCars;
    }

    public void setNumberOfCars(int numberOfCars) {
        this.numberOfCars = numberOfCars;
    }

    public int getNumberOfFridges() {
        return numberOfFridges;
    }

    public void setNumberOfFridges(int numberOfFridges) {
        this.numberOfFridges = numberOfFridges;
    }

    public int getNumberOfTanks() {
        return numberOfTanks;
    }

    public void setNumberOfTanks(int numberOfTanks) {
        this.numberOfTanks = numberOfTanks;
    }
    
    
}
